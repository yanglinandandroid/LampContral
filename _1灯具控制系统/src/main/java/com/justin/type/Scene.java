package com.justin.type;

public class Scene {

	private String name = "";
	private String id   = "";
	private String value = "";
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return this.name;
	}
	
	
	public void setId(String id)
	{
		this.id = id;
	}
	
	
	public String getid ()
	{
		return this.id;
	}
	
	public void setValue(String value)
	{
		this.value = value;
	}
	
	
	public String getValue()
	{
		return this.value;
	}
}
